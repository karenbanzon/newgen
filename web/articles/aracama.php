<p>Well known Chef Fernando &ldquo;Fern&rdquo; Aracama recalls fond memories of Christmas past in his cooking class at The Maya Kitchen on November 23, Saturday from 9am&ndash;1pm.

A very young boy walking home from midnight mass with his family is something Chef Fern remembers to this day. He took inspiration from the traditional Media Noche meal that awaited them but this time he shares recipes of dishes that are not the usual but delightful nonetheless.

To warm you up is Patatas Riojana, a very simple yet hearty and delicious potato and chorizo soup Chef Fern revisited on his recent trip to Spain.

To fill you up is Grilled Ensaimada Media Noche style. Hot and crusty butter grilled ensaimada stuffed with Chinese ham and queso de bola.

Insalata Uvato goes with the Grilled Ensaimada. This is his signature salad with honey balsamico vinaigrette, toasted pine nuts, green and red grapes and kesong puti.

Malunggay and Mozzarella dip is nutritious greens in a creamy, cheesy br&ucirc;le&eacute; best served with crispy crostini. This is a signature appetizer in Aracama Filipino Cuisine, one of Chef Fern&rsquo;s restaurants.

And because it is Christmas there has to be Bunuelos and Warm Chocolate Dip. These are the recipes of Chef Fern&rsquo;s dear Abuelita of deep fried choux pastries served with a thick and smooth milk chocolate dip.

Chef Fern hails from Bacolod and is a graduate of the University of the Philippines with a degree in Hotel and Restaurant Administration. He took further studies at The New England Culinary Institute in Montpelier, Vermont USA and was a 2003 Cochran Fellowship Program Awardee at the Hilton College, University Of Houston, Texas, USA.

Chef Fern has a long list of bars and restaurants to his name like Aracama Filipino Cuisine, Opus, Republiq &amp; Caf&eacute; Republiq, MYTHAI Kitchen, Lucky Niku Caf&eacute;, The ChopHouse, The Tides Hotel Boracay as well as previous spots Restaurant UVA, Encore/Embassy &amp; Member&rsquo;s Only and CANTEEN simple+good food.

He is also active in World Association of Chef Societies (WACS), Les Toques Blances Philippines, Council of Chefs, United States of America&rsquo;s Department of Agriculture and is a Chef Consultant with Philippine Airlines.</p>
